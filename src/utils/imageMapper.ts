/**
 * Returns a high-quality, beautiful, guaranteed photo URL for any given word.
 * If the card is a custom AI-generated option, we synthesize a dynamic search
 * query based on the search term, or default gracefully to high-vibe premium backgrounds.
 */
export function getVocabularyImage(englishWord: string, category: 'home' | 'office' | 'others', customTerm?: string): string {
  const normWord = englishWord.toLowerCase().trim();

  // 1. Direct match on hand-picked items
  if (category === 'home' && HOME_IMAGES[normWord]) return HOME_IMAGES[normWord];
  if (category === 'office' && OFFICE_IMAGES[normWord]) return OFFICE_IMAGES[normWord];
  if (category === 'others' && OTHERS_IMAGES[normWord]) return OTHERS_IMAGES[normWord];

  // Try crossing categories
  if (HOME_IMAGES[normWord]) return HOME_IMAGES[normWord];
  if (OFFICE_IMAGES[normWord]) return OFFICE_IMAGES[normWord];
  if (OTHERS_IMAGES[normWord]) return OTHERS_IMAGES[normWord];

  // 2. Fallbacks for AI-generated dynamic cards or custom search
  const searchString = customTerm || normWord || "study";
  const formattedSearch = encodeURIComponent(searchString.replace(/\s+/g, ','));
  
  // Return high-relevance dynamic search keyword image from loremflickr
  return `https://loremflickr.com/600/400/${formattedSearch}/all`;
}

